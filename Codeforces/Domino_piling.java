package Codeforces;

import java.util.*;

//https://codeforces.com/problemset/problem/50/A
public class Domino_piling {

	public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int m=sc.nextInt();
        int ans=(n*m)/2;
        System.out.println(ans);
        sc.close();
    }
	
}
